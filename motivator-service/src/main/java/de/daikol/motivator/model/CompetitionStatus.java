package de.daikol.motivator.model;


/**
 * This enumeration lists all possible status for a competition.
 *
 * @author daikol
 * @version 1.0
 */
public enum CompetitionStatus {
    /**
     * This status indicates that the competitor has confirmed the competition.
     */
    CONFIRMED,
    /**
     * This status indicates that the competitor do not want to accept the competition.
     */
    DECLINED;
}